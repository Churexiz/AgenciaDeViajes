package capanegocio;

public class Proveedores {
    private String vuelos;
    private String cruceros;
    private String reservaHotel;
    private String alquilerAuto;
    private String seguroDeViaje;
    private String paquetesVacacionales;
    private String actividades;

    public String getVuelos() {
        return vuelos;
    }

    public void setVuelos(String vuelos) {
        this.vuelos = vuelos;
    }

    public String getCruceros() {
        return cruceros;
    }

    public void setCruceros(String cruceros) {
        this.cruceros = cruceros;
    }

    public String getReservaHotel() {
        return reservaHotel;
    }

    public void setReservaHotel(String reservaHotel) {
        this.reservaHotel = reservaHotel;
    }

    public String getAlquilerAuto() {
        return alquilerAuto;
    }

    public void setAlquilerAuto(String alquilerAuto) {
        this.alquilerAuto = alquilerAuto;
    }

    public String getSeguroDeViaje() {
        return seguroDeViaje;
    }

    public void setSeguroDeViaje(String seguroDeViaje) {
        this.seguroDeViaje = seguroDeViaje;
    }

    public String getPaquetesVacacionales() {
        return paquetesVacacionales;
    }

    public void setPaquetesVacacionales(String paquetesVacacionales) {
        this.paquetesVacacionales = paquetesVacacionales;
    }

    public String getActividades() {
        return actividades;
    }

    public void setActividades(String actividades) {
        this.actividades = actividades;
    }
    
}
